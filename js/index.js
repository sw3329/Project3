function main() {

  // Creates constants based off of the HTML created
  const $textbox = document.querySelector("#textbox p");
  const $optionsbox = document.querySelector('#optionsbox');
  const $spritebox = document.querySelector("#spritebox img");
  const $ndspritebox = document.querySelector("#ndspritebox img");
  const $spritebox1 = document.querySelector("#spritebox1 img");
  const $ndspritebox1 = document.querySelector("#ndspritebox1 img");
  const $mainbox = document.querySelector('#mainbox');
  const $bgm = document.querySelector('#bgm');
  const $effect = document.querySelector('#effect');
  const $textsound = document.querySelector('#textsound');

  let json, to;
  var pageNum = 0;
  var currentPage;

  async function grabData() {

    const resp = vnData

    json = await resp;

    currentPage = Object.keys(json.Scene1.PAGES)[pageNum];

    initialize(json);
    handleOptions(json);
  }

  async function initialize(data) {

    $spritebox1.src = '';
    $ndspritebox1.src = '';
    $spritebox.src = '';
    $ndspritebox.src = '';

    $textbox.innerText = '';
    $spritebox.src = data.Scene1.PAGES[currentPage].Sprite;
    $ndspritebox.src = data.Scene1.PAGES[currentPage].Sprite2;
    $spritebox1.src = data.Scene1.PAGES[currentPage].darkRight;
    $ndspritebox1.src = data.Scene1.PAGES[currentPage].darkLeft;
    $mainbox.style.backgroundImage = `url(${data.Scene1.PAGES[currentPage].Background})`;
    typeWriter(data.Scene1.PAGES[currentPage].PageText)
    // insert bgm, "no", then stop, if new file incoming, then play the music
    const bgm = data.Scene1.PAGES[currentPage].bgm;
    if (bgm) {
      if (bgm === 'no') {
        $bgm.pause();
      } else {
        $bgm.src = bgm;
        $bgm.play();
      }
    }
    // do effect
    const effect = data.Scene1.PAGES[currentPage].effect;
    if (effect) {
      $effect.src = effect;
      $effect.play();
    }
  }

  function handleOptions(data) {


    $optionsbox.innerHTML = "";

    if (data.Scene1.PAGES[currentPage].hasOwnProperty('Options')) {
      var o = data.Scene1.PAGES[currentPage].Options;
      var str = Object.keys(o).forEach(k => {
        const row = document.createElement('div');
        row.innerHTML = `${k}`
        $optionsbox.appendChild(row);
        row.addEventListener('click', () => {
          currentPage = (o[k]);
          pageNum = Object.keys(json.Scene1.PAGES).indexOf(currentPage);
          initialize(json);
          $optionsbox.innerHTML = "";
        })

      })
    }


  }


  function typeWriter(txt, i) {
    i = i || 0;
    if (!i) {
      $textbox.innerHTML = '';
      clearTimeout(to);
    }
    var speed = 30; /* The speed/duration of the effect in milliseconds */
    if (i < txt.length) {
      var c = txt.charAt(i++);
      $textsound.play();
      $textsound.currentTime = 0;
      if (c === ' ') c = '&nbsp;'
      $textbox.innerHTML += c;
      to = setTimeout(function() {
        typeWriter(txt, i)
      }, speed);
    }
  }

  function checkPage(data) {
    if (data.Scene1.PAGES[currentPage].hasOwnProperty('Options')) return false;
    if (data.Scene1.PAGES[currentPage].hasOwnProperty('NextPage')) {
      if (data.Scene1.PAGES[currentPage].NextPage == "End") return false;
    }

    return true;
  }



  //Handles page turning when right or left arrow key is pressed 
  document.addEventListener('keydown', (e) => {
    if (!json) return;
    if (e.key == "ArrowRight" && checkPage(json)) {

      if (json.Scene1.PAGES[currentPage].hasOwnProperty('NextPage')) {
        currentPage = json.Scene1.PAGES[currentPage].NextPage;
      } else {
        pageNum++;
        currentPage = Object.keys(json.Scene1.PAGES)[pageNum];
      }

      initialize(json);
      handleOptions(json);
    } else return;
  })

  //Grabs the json data from the server
  grabData();
}